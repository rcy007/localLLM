def add(a, b):
    """
    Add two numbers and return the result.

    Parameters:
    a (int or float): The first number.
    b (int or float): The second number to add to the first.

    Returns:
    int or float: The result of the addition.
    """
    return a + b

# Example usage
if __name__ == "__main__":
    num1 = 10
    num2 = 5
    result = add(num1, num2)
    print(f"The result of adding {num2} to {num1} is: {result}")

